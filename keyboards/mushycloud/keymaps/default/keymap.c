bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case QWERTY:
            if (record->event.pressed) {
                set_single_persistent_default_layer(_QWERTY);
            }
            return false;
        break;
        case LOWER:
            if (record->event.pressed) {
                layer_on(_LOWER);
                update_tri_layer(_LOWER, _RAISE, _FN);
            } else {
                layer_off(_LOWER);
                update_tri_layer(_LOWER, _RAISE, _FN);
            }
            return false;
        break;
        case RAISE:
            if (record->event.pressed) {
                layer_on(_RAISE);
                update_tri_layer(_LOWER, _RAISE, _FN);
            } else {
                layer_off(_RAISE);
                update_tri_layer(_LOWER, _RAISE, _FN);
            }
            return false;
        break;
        case FN:
            if (record->event.pressed) {
                layer_on(_FN);
            } else {
                layer_off(_FN);
            }
            return false;
        break;
    }
    return true;
}

